void UART_print(char* format, ...){

	char buffer[100];
	uint8_t size = 0;
	buffer[0] = '\0';

	va_list argList;

	va_start(argList, format);
	size = vsprintf(buffer, format, argList);

	HAL_UART_Transmit(&huart1, (uint8_t *)buffer, size, 10);

	va_end(argList);

}


void sendString(int id, ...) {
	va_list args;
	va_start(args, id);

	char xbeeBuffer[128];
	uint16_t data_word[4];

	for (int i = 0; i < 4; i++) {
		data_word[i] = (uint16_t) va_arg(args, int);
	}
	va_end(args);

	int len = sprintf(xbeeBuffer, "%u\t%u\t%u\t%u\t%u\n", id, data_word[0], data_word[1], data_word[2], data_word[3]);
	HAL_UART_Transmit (&huart1, (uint8_t*)xbeeBuffer, len, 100);
	DWT_Delay_us(1000);
}


void UART_print_float(float data){
	UART_print("%2d,%0.3d", (int16_t)data, (int16_t)abs((data*1000) - ((int16_t)data*1000)));


}

void UART_print_error_flag(uint16_t flag){

	if(flag == ERR_NO_ERROR)
		UART_print("    ");
	if((flag & ERR_OVER_VOLTAGE) == ERR_OVER_VOLTAGE)
		UART_print("[OV]");
	if((flag & ERR_UNDER_VOLTAGE) == ERR_UNDER_VOLTAGE)
		UART_print("[UV]");
	if((flag & ERR_OVER_CURRENT) == ERR_OVER_CURRENT)
		UART_print("[OC]");
	if((flag & ERR_OVER_TEMPERATURE) == ERR_OVER_TEMPERATURE)
		UART_print("[OT]");
	if((flag & ERR_OPEN_FUSES) == ERR_OPEN_FUSES)
		UART_print("[FU]");
	if((flag & ERR_COMM_ERROR) == ERR_COMM_ERROR)
		UART_print("[TC]");
	if((flag & ERR_AIR_ERROR) == ERR_AIR_ERROR)
		UART_print("[AIR]");
	if((flag & ERR_GLV_VOLTAGE) == ERR_GLV_VOLTAGE)
		UART_print("[GLV]");
	if((flag & ERR_BALANCE) == ERR_BALANCE)
		UART_print("[BAL]");
}

